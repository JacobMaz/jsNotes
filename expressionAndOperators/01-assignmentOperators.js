//assignment operator
let r = 5;
//    ^

//addition assignment
r += 1; // same as: r = r + 1

//subtraction assignment
r -= 1 // same as: r = r - 1

//multiplication assignment
r *= 1 // same as: r = r * 1

//division assignment

r /= 1 // same as: r = r / 1

//remainder (modulus) assignment
r %= 1 // r = r % 1

//exponetial assignment
r **= 1 // r = r ** 1